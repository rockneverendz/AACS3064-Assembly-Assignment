.MODEL SMALL
.STACK 100
.DATA
	NUM DB 147
	TEN DB 10
	NL DB 0DH,0AH,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	;----BEGIN----

	MOV AH, 0
	MOV AL, NUM;--AH = 0 ; AL = 137
	DIV TEN    ;--AH = 7 ; AL = 13

	MOV BX, AX ;--BH = 7 ; BL = 13
	MOV AL, BL
	MOV AH, 0  ;--AH = 0 ; AL = 13
	DIV TEN    ;--AH = 3 ; AL = 1
	MOV CH, AH ;
	
	           ;INTEGER
	MOV AH,02H ;--MANDATORY FOR PRINT VARIABLE(MOVE ONCE)
	MOV DL,AL  ;--MOVING VARIABLE TO PRINT ()
	ADD DL,48  ;--OFFSETTING ASCII VALUES (0 BEGINS AT 48)
	INT 21H	   ;--MANDATORY FOR PRINT

	           ;INTEGER
	MOV DL,CH  ;--MOVING VARIABLE TO PRINT ()
	ADD DL,48  ;--OFFSETTING ASCII VALUES (0 BEGINS AT 48)
	INT 21H	   ;--MANDATORY FOR PRINT

	           ;INTEGER
	MOV DL,BH  ;--MOVING VARIABLE TO PRINT ()
	ADD DL,48  ;--OFFSETTING ASCII VALUES (0 BEGINS AT 48)
	INT 21H	   ;--MANDATORY FOR PRINT

	
	
	;----END----
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN