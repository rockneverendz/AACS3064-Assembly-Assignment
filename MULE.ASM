.MODEL SMALL
.STACK 100
.DATA
	STR1 DB "SUPER"
	NL DB 0DH,0AH,"$"
	TEN DB 10
	
	NUMBER1 DB 12
	NUMBER2 DB 8
	
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	;----BEGIN----
	MOV AH, 0
	MOV AL, NUMBER1
	MUL NUMBER2
	
					;DIVISION
	MOV AH,0	;
	DIV TEN		;AL = QUOTIENT, AH = REMAINDER
	MOV BX, AX	;PREVENT DATA LOSS
	
				;2ND DIGIT
	MOV AH,02H	;
	MOV DL,BL	;
	ADD DL,48	;
	INT 21H		;
	
				;1ST DIGIT
	MOV DL,BH	;
	ADD DL,48	;
	INT 21H		;

	
	
	;----END----
FINISH:	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN