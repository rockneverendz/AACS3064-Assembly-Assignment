.MODEL SMALL
.STACK 100
.DATA
	VAL1 DB ?
	VAL2 DB ?
 	STR1 DB 0DH,0AH,"ENTER TWO ODD INTEGER VALUES (0 TO 9) : $"
	STR2 DB 0DH,0AH,"THE AVERAGE OF THE INPUT VALUES IS    : $"
	STR3 DB ".0$"
	TEN DB 10
	TWO DB 2
	NL DB 0DH,0AH,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	;----BEGIN----

	           ;STRING
	MOV AH,09H ;--MANDATORY FOR PRINT STRING(MOVE ONCE)
	LEA DX,STR1;--LOADING STRING TO PRINT
	INT 21H    ;--MANDATORY FOR PRINT

	           ;INPUT SINGLE CHARACTER
	MOV AH,01H ;
	INT 21H    ;
	SUB AL, 48 ;(MINUS 48 IF NUMBER)
	MOV VAL1, AL

	           ;INTEGER
	MOV AH,02H ;--MANDATORY FOR PRINT VARIABLE(MOVE ONCE)
	MOV DL,"," ;--MOVING VARIABLE TO PRINT ()
	INT 21H	   ;--MANDATORY FOR PRINT

	           ;INPUT SINGLE CHARACTER
	MOV AH,01H ;
	INT 21H    ;
	SUB AL, 48 ;(MINUS 48 IF NUMBER)
	MOV VAL2, AL
	
	ADD AL, VAL1
	MOV AH, 0
	DIV TWO
	

	           ;STRING
	MOV AH,09H ;--MANDATORY FOR PRINT STRING(MOVE ONCE)
	LEA DX,STR2;--LOADING STRING TO PRINT
	INT 21H    ;--MANDATORY FOR PRINT

	           ;INTEGER
	MOV AH,02H ;--MANDATORY FOR PRINT VARIABLE(MOVE ONCE)
	MOV DL,AL  ;--MOVING VARIABLE TO PRINT ()
	ADD DL,48  ;--OFFSETTING ASCII VALUES (0 BEGINS AT 48)
	INT 21H	   ;--MANDATORY FOR PRINT


	           ;STRING
	MOV AH,09H ;--MANDATORY FOR PRINT STRING(MOVE ONCE)
	LEA DX,STR3;--LOADING STRING TO PRINT
	INT 21H    ;--MANDATORY FOR PRINT
	
	
	;----END----
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN