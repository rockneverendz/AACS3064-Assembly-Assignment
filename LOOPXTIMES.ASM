.MODEL SMALL
.STACK 100
.DATA
	STR1 DB 0DH,0AH,"ENTER A CHARACTER : $"
	STR2 DB 0DH,0AH,"ENTER A NUMBER    : $"
	CHAR DB "9"
	NL DB 0DH,0AH,"$"

.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	;----BEGIN----

	           ;STRING
	MOV AH,09H ;--MANDATORY FOR PRINT STRING(MOVE ONCE)
	LEA DX,STR1;--LOADING STRING TO PRINT
	INT 21H    ;--MANDATORY FOR PRINT

	           ;INPUT SINGLE CHARACTER
	MOV AH,01H ;
	INT 21H    ;
	MOV BL, AL

	           ;STRING
	MOV AH,09H ;--MANDATORY FOR PRINT STRING(MOVE ONCE)
	LEA DX,STR2;--LOADING STRING TO PRINT
	INT 21H    ;--MANDATORY FOR PRINT

	           ;INPUT SINGLE CHARACTER
	MOV AH,01H ;
	INT 21H    ;
	SUB AL, 48
	MOV CH, 0	
	MOV CL, AL

	           ;STRING
	MOV AH,09H ;--MANDATORY FOR PRINT STRING(MOVE ONCE)
	LEA DX,NL  ;--LOADING STRING TO PRINT
	INT 21H    ;--MANDATORY FOR PRINT

	SHOW:
		MOV AH,02H ;--MANDATORY FOR PRINT VARIABLE(MOVE ONCE)
		MOV DL,BL  ;--MOVING VARIABLE TO PRINT ()
		INT 21H	   ;--MANDATORY FOR PRINT
	LOOP SHOW
	
	
	;----END----
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN