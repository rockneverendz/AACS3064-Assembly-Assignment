.MODEL SMALL
.STACK 100
.DATA
	STR1 DB 0DH,0AH,"ENTER FIRST DIGIT  : $"
	STR2 DB 0DH,0AH,"ENTER SECOND DIGIT : $"
	NUM1 DB ?
	NL DB 0DH,0AH,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	;----BEGIN----

		       ;STRING
	MOV AH,09H ;--MANDATORY FOR PRINT STRING(MOVE ONCE)
	LEA DX,STR1;--LOADING STRING TO PRINT
	INT 21H    ;--MANDATORY FOR PRINT

				;INPUT SINGLE CHARACTER
	MOV AH,01H ;
	INT 21H    ;
	SUB AL,48  ;(MINUS 48 IF NUMBER)
	MOV NUM1, AL;
	
		       ;STRING
	MOV AH,09H ;--MANDATORY FOR PRINT STRING(MOVE ONCE)
	LEA DX,STR2;--LOADING STRING TO PRINT
	INT 21H    ;--MANDATORY FOR PRINT

		        ;INPUT SINGLE CHARACTER
	MOV AH,01H  ;
	INT 21H     ;
	SUB AL,48   ;(MINUS 48 IF NUMBER)
	SUB AL, NUM1;
		
	MOV CH,0   ;
	MOV CL,AL  ;
	INC CL     ; TO DISPLAY THE CECOND DIGIT ITSELF
	
	MOV AH,09H ;--
	LEA DX,NL  ;--NEW LINE
	INT 21H    ;--

	
SHOW:
		           ;INTEGER
	MOV AH,02H ;--MANDATORY FOR PRINT VARIABLE(MOVE ONCE)
	MOV DL,NUM1 ;--MOVING VARIABLE TO PRINT ()
	ADD DL,48;
	INT 21H	   ;--MANDATORY FOR PRINT
	
	INC NUM1;
	LOOP SHOW;

	;----END----
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN