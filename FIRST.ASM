.MODEL SMALL
.STACK 100
.DATA
	LIST   DB 12, 29, -9, 5, -48,7 ,20,-10, 6,-99, 78, 0
	COUNTN DB 0
	COUNTP DB 0
	POSIs  DB 0DH,0AH,"NUMBER OF POSITIVE INT : $"
	NEGEs  DB 0DH,0AH,"NUMBER OF NEGATIVE INT : $"
.CODE

MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	;----BEGIN----

	MOV SI, 0
	J1:
		CMP LIST[SI], 0
		JE  OUTT
		JL  NEGE
		INC COUNTP
		JMP NEXT
		
		NEGE:
		INC COUNTN
		
		NEXT:
		INC SI
	JMP J1
	OUTT:
	
	MOV AH, 09H
	LEA DX, POSIs
	INT 21H
	
				;CHARACTER
	MOV AH,02H	;
	MOV DL,COUNTP	;
	ADD DL,48	;--OFFSETTING ASCII VALUES (0 BEGINS AT 48)
	INT 21H		;

	
	MOV AH, 09H
	LEA DX, NEGEs
	INT 21H
	
				;CHARACTER
	MOV AH,02H	;
	MOV DL,COUNTN	;
	ADD DL,48	;--OFFSETTING ASCII VALUES (0 BEGINS AT 48)
	INT 21H		;

	
	;----END----
FINISH:	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN