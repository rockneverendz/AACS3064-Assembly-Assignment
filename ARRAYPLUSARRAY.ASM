.MODEL SMALL
.STACK 100
.DATA
	NUM1 DB 4,7,2,9
	NUM2 DB 10,8,5,3
	STR1 DB 0DH,0AH,"SUM IS $"
	TEN DB 10
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	;----BEGIN----
	
	MOV CX, 4
	MOV SI, 0
	
	L1:
		MOV AH, 0
		MOV AL, NUM1[SI]
		ADD AL, NUM2[SI]
		DIV TEN		;AL = QUOTIENT, AH = REMAINDER
		MOV BX, AX	;PREVENT DATA LOSS
		
					;INTEGER
		MOV AH,02H	;
		MOV DL,BL	;
		ADD DL,48	;
		INT 21H		;
					;INTEGER
		MOV DL,BH	;
		ADD DL,48	;
		INT 21H		;

		MOV DL," "	;
		INT 21H		;

		
		INC SI;
	LOOP L1;
		
	
	;----END----
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN