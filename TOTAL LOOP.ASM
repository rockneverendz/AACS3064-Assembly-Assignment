.MODEL SMALL
.STACK 100
.DATA
	NUM1 DB "1"
	SUM DB 0
	STR1 DB 0DH,0AH,"ENTER A DIGIT : $"
	STR2 DB 0DH,0AH,"TOTAL IS      : $"
	NL DB 0DH,0AH,"$"
	TEN DB 10
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	;----BEGIN----

	MOV CX, 4;
	
SHOW:
				;STRING
	MOV AH,09H	;
	LEA DX,STR1	;--ENTER A DIGIT 
	INT 21H		;

				;INPUT SINGLE CHARACTER
	MOV AH,01H	;
	INT 21H		;
	SUB AL,48	;(MINUS 48 IF NUMBER)
	ADD SUM, AL	;
	
	LOOP SHOW;
	
				;DIVISION
	MOV AH,0	;
	MOV AL,SUM	;
	DIV TEN		;AL = QUOTIENT, AH = REMAINDER
	MOV BX, AX	;PREVENT DATA LOSS
	
				;STRING
	MOV AH,09H	;
	LEA DX,STR2	;--LOADING STRING TO PRINT
	INT 21H		;

				;INTEGER
	MOV AH,02H	;
	MOV DL,BL	;
	ADD DL,48	;
	INT 21H		;
	
				;INTEGER
	MOV DL,BH	;
	ADD DL,48	;
	INT 21H		;

	
	
	
	;----END----
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN